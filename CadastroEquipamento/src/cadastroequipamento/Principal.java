    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed

        int quant = Integer.parseInt(txtquantidade.getText());
        if (modo.equals("Novo")) {

            Equipamento E = new Equipamento(txtnome.getText(), quant, txtsku.getText());
            ListEqp.add(E);
        } else if (modo.equals("Editar")) {
            int index = tblequipamentos.getSelectedRow();
            ListEqp.get(index).setNome(txtnome.getText());
            ListEqp.get(index).setQuantidade(quant);
            ListEqp.get(index).setSku(txtsku.getText());
        }
        LoadTableEqp();

        modo = "Navegar";
        ManipulaInterface();
        txtnome.setText("");
        txtquantidade.setText("");
        txtsku.setText("");

    }//GEN-LAST:event_btnsalvarActionPerformed

    private void tblequipamentosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblequipamentosMouseClicked
        int index = tblequipamentos.getSelectedRow();

        if (index >= 0 && index < ListEqp.size()) {

            Equipamento E = ListEqp.get(index);
            txtnome.setText(E.getNome());
            txtquantidade.setText(String.valueOf(E.getQuantidade()));
            txtsku.setText(E.getSku());
            modo = "Selecao";
            ManipulaInterface();

        }
    }//GEN-LAST:event_tblequipamentosMouseClicked

