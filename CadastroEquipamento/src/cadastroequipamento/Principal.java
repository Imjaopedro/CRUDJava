    public void LoadTableEqp() {
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"Nome", "Quantidade", "SKU"}, 0);

        for (int i = 0; i < ListEqp.size(); i++) {
            Object linha[] = new Object[]{ListEqp.get(i).getNome(), ListEqp.get(i).getQuantidade(), ListEqp.get(i).getSku()};

            modelo.addRow(linha);
        }

        tblequipamentos.setModel(modelo);
        tblequipamentos.getColumnModel().getColumn(0).setPreferredWidth(200);
        tblequipamentos.getColumnModel().getColumn(1).setPreferredWidth(100);
        tblequipamentos.getColumnModel().getColumn(2).setPreferredWidth(100);
    }

